from __future__ import annotations

from aragora.server.openapi import generate_openapi_schema


HTTP_METHODS = {"get", "post", "put", "patch", "delete", "options", "head"}


def _operation_methods(paths: dict[str, dict[str, object]], path: str) -> set[str]:
    return {method for method in paths[path] if method in HTTP_METHODS}


def test_settings_api_key_alias_routes_are_curated() -> None:
    paths = generate_openapi_schema()["paths"]

    expected = {
        "/api/api-keys": {
            "get": None,
            "post": None,
            "delete": None,
        },
        "/api/v1/api-keys": {
            "get": "listSettingsApiKeysV1",
            "post": "createSettingsApiKeyV1",
            "delete": "revokeSettingsApiKeyV1",
        },
        "/api/api-keys/{prefix}": {
            "delete": None,
        },
        "/api/v1/api-keys/{prefix}": {
            "delete": "revokeSettingsApiKeyByPrefixV1",
        },
    }

    for path, operations in expected.items():
        assert _operation_methods(paths, path) == set(operations)
        for method, operation_id in operations.items():
            operation = paths[path][method]
            assert isinstance(operation, dict)
            if operation_id is not None:
                assert operation.get("operationId") == operation_id
            assert operation.get("x-autogenerated") is not True
            assert operation["tags"] == ["Authentication", "API Keys"]


