def test_auth_api_key_singular_route_does_not_document_unsupported_get() -> None:
    paths = generate_openapi_schema()["paths"]

    assert _operation_methods(paths, "/api/auth/api-key") == {"post", "delete"}
    assert _operation_methods(paths, "/api/v1/auth/api-key") == {"post", "delete"}


def test_api_key_prefix_routes_include_path_parameters() -> None:
    paths = generate_openapi_schema()["paths"]

    for path in (
        "/api/api-keys/{prefix}",
        "/api/v1/api-keys/{prefix}",
        "/api/auth/api-keys/{prefix}",
        "/api/v1/auth/api-keys/{prefix}",
    ):
        operation = paths[path]["delete"]
        assert isinstance(operation, dict)
        parameters = operation.get("parameters")
        assert isinstance(parameters, list)
        assert parameters == [
            {
                "name": "prefix",
                "in": "path",
                "required": True,
                "description": "API key prefix to revoke.",
                "schema": {"type": "string"},
            }
        ]
