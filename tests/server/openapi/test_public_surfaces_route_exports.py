    assert operation["operationId"] == "getPublicSurfaces"
    assert operation["summary"] == "List public surface readiness"
    assert operation["tags"] == ["Status", "Public", "Readiness"]
    assert operation["security"] == []
    assert operation.get("x-autogenerated") is not True

    response_schema = operation["responses"]["200"]["content"]["application/json"]["schema"]
    data_schema = response_schema["properties"]["data"]
    surface_schema = data_schema["properties"]["surfaces"]["items"]
    summary_schema = data_schema["properties"]["summary"]

    assert "surfaces" in data_schema["required"]
    assert "summary" in data_schema["required"]
    assert "placeholder_backed" in surface_schema["required"]
    assert surface_schema["properties"]["readiness"]["enum"] == ["live", "partial"]
    assert set(summary_schema["required"]) == {"total", "live", "partial"}
