from __future__ import annotations

from aragora.server.handlers import ALL_HANDLERS
from aragora.server.handlers.public.status_page import StatusPageHandler
from aragora.server.openapi import generate_openapi_schema
import pytest


HTTP_METHODS = {"get", "post", "put", "patch", "delete", "head", "options"}


def _operation_methods(path_spec: dict[str, object]) -> set[str]:
    return {method for method in path_spec if method in HTTP_METHODS}


def test_status_page_handler_participates_in_all_handlers() -> None:
    assert StatusPageHandler in set(ALL_HANDLERS)


def test_public_surfaces_route_is_public_and_handled() -> None:
    handler = StatusPageHandler()

    assert "/api/v1/public/surfaces" in StatusPageHandler.PUBLIC_ROUTES
    assert handler.can_handle("/api/v1/public/surfaces")


